# Ndarray数据的创建、切片与索引

import numpy as np

# 创建一个一维数组
arr1 = np.array([5.85, 5.95, 5.85, 5.90, 6.10, 6.00])
# 创建切片对象，从索引位置1到4，默认步长是1
s1 = slice(1, 4)
print(arr1[s1])
# 创建切片对象，从索引位置1到5，步长为2
s2 = slice(1, 5, 2)
print(arr1[s2])

# 创建二维数组
arr2 = np.array([[5.85, 5.95, 5.85], [5.90, 6.10, 6.00], [5.95, 6.15, 6.05]])

# 索引行位置1，列位置1的数据
print(arr2[1, 1])
# 索引行位置2，列位置2的数据
print(arr2[2, 2])

# 行索引1到最后的切片
print(arr2[1:])
# 行索引0到2，列索引0到2的切片
print(arr2[0:2, 0:2])
# 行索引1到最后，列索引1到最后的切片
print(arr2[1:, 1:])
